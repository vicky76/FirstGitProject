package com.discount;

public class Discount {
	
	private int discountInPercentage;
	
	private int absoluteDiscount;

	public int getDiscountInPercentage() {
		return discountInPercentage;
	}

	public void setDiscountInPercentage(int discountInPercentage) {
		this.discountInPercentage = discountInPercentage;
	}

	public int getAbsoluteDiscount() {
		return absoluteDiscount;
	}

	public void setAbsoluteDiscount(int absoluteDiscount) {
		this.absoluteDiscount = absoluteDiscount;
	}

}
